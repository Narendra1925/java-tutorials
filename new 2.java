class Calculator {
    public static void main(String[] args) {
        int x = 10;
        float y = 20;
        System.out.println("Addition = " + (x+y));
        System.out.println("Substraction = " + (x-y));
        System.out.println("Multiplication = " + (x*y));
        System.out.println("Division = " + (x/y));
    }
}